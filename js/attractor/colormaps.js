export function colormapRGB(name, t) {
  if (t < 0) t = 0;
  else if (t > 1) t = 1;
  if (name === "grayscale") return [t, t, t];
  if (name === "viridis") return poly(t, ...VIRIDIS);
  if (name === "magma") return poly(t, ...MAGMA);
  if (name === "inferno") return poly(t, ...INFERNO);
  if (name === "plasma") return poly(t, ...PLASMA);
  return [t, t, t];
}

// 256-entry RGBA8 LUT for use as a sampler2D in the tonemap shader.
export function buildLUT(name) {
  const lut = new Uint8Array(256 * 4);
  for (let i = 0; i < 256; i++) {
    const [r, g, b] = colormapRGB(name, i / 255);
    lut[i * 4 + 0] = Math.max(0, Math.min(255, Math.round(r * 255)));
    lut[i * 4 + 1] = Math.max(0, Math.min(255, Math.round(g * 255)));
    lut[i * 4 + 2] = Math.max(0, Math.min(255, Math.round(b * 255)));
    lut[i * 4 + 3] = 255;
  }
  return lut;
}

