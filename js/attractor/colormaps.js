// Polynomial colormap approximations (Matt Zucker / ShaderToy
// "Optimized colormaps"). Each returns an [r, g, b] triple in 0..1
// for input t in 0..1.

function poly(t, c0, c1, c2, c3, c4, c5, c6) {
  const r = c0[0] + t * (c1[0] + t * (c2[0] + t * (c3[0] + t * (c4[0] + t * (c5[0] + t * c6[0])))));
  const g = c0[1] + t * (c1[1] + t * (c2[1] + t * (c3[1] + t * (c4[1] + t * (c5[1] + t * c6[1])))));
  const b = c0[2] + t * (c1[2] + t * (c2[2] + t * (c3[2] + t * (c4[2] + t * (c5[2] + t * c6[2])))));
  return [r, g, b];
}

